public class Animal {
    private int patas;
    private String raca;
    private String nome = "Lobo";

    public void setPatas(int patas) {
        this.patas = patas;
    }

    public void setRaca(String raca) {
        this.raca = raca;
    }

    public int getPatas() {
        return patas;
    }

    public String getRaca() {
        return raca;
    }

    public String getNome() {
        return this.nome;
    }
}
